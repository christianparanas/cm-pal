		<p>
			<b>Algebraic Equation</b><br>
			<br><b>Algebraic equation</b>, statement of the equality of two expressions formulated by applying to a set of variables
			the algebraic operations, namely, addition, subtraction, multiplication, division,raising to a power, and extraction
			a root. Examples are <i>x<sup>3</sup> + 1</i> and (<i>y<sup>4</sup>x<sup>3</sup> + 2xy - y</i>)/(<i>x-1</i>) = 12. An important special 
			case of such equations, expresssions of the form <i>axn + bxn<sup>-1</sup> + _ + gx + h=k</i>. They have as many solutions as 
			their degree(<i>n</i>), and the search for their solutions stimulated much of the development of classical and modern algebra. 
			Equation like <i>x</i> sin (<i>x</i>) = <i>c</i> that involve nonalgebraic operations, such as logarithms or trigonometric 
			functions, are said to be transcendental.<br>
		</p>
		<p>
			<br><b>Chemical Compound</b><br>
			<br>A <b>chemical compound</b>, is a chemical substance consisting of two or more different chemically bonded chemical elements, with a
			fixed ratio determining the composition.
			<br>The ratio of each element is usually expressed by chemical formula.
			<br>For example, water (H<sub>2</sub>O) is a compound consisting of two hydrogen atoms bonded to an oxygen atom.
			<br>The atoms within a compound can be held together by a variety of interactions, ranging from covalent bonds to 
			electrostatic forces on ionic bonds.
			<br>A continuum of bond polarities exist between the purely covalent bond (as in H2) and ionic bonds.
			<br>For example H<sub>2</sub>O is held together by polar covalent bonds.
			<br>Sodium chloride is an example of an ionic compound.<br>
		</p>

